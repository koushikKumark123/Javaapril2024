package comjavatraining.basic;

public class EncapsulationEX {
	private String name;
	private int id;
	private int age;
	//alt+shift+s--->For generate setter and getter
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
//this, this(),super, super(),final,static,access specifier
}



